#include "libft.h"

size_t	ft_strlcat(char *dest, const char *src, size_t size)
{
	size_t	i;
	size_t	j;

	i = 0;
	j = 0;
	if (ft_strlen(dest) > size)
		return (ft_strlen(src) + size);
	else
		while (dest[i] != '\0' && i < size && size > 0)
			i++;
	while ((i + j + 1) < size && src[j] != '\0')
	{
		dest[i + j] = src[j];
		j++;
	}
	dest[i + j] = '\0';
	return (i + ft_strlen(src));
}
